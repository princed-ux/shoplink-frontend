export function getCountryByCode(code) {
  return (
    COUNTRIES.find((c) => c.code === code) ||
    COUNTRIES.find((c) => c.code === "NG")
  );
}

export function getCurrency(countryCode) {
  const c = getCountryByCode(countryCode);
  return c?.currency || "USD";
}

export function getDialCode(countryCode) {
  const c = getCountryByCode(countryCode);
  return c?.dial || "+234";
}

export function formatPhone(phone, countryCode) {
  const dial = getDialCode(countryCode);
  const n = phone.trim().replace(/\s+/g, "");
  if (n.startsWith("+")) return n;
  if (n.startsWith(dial.replace("+", ""))) return "+" + n;
  return dial + n.replace(/^0+/, "");
}
